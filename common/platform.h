/* offsets for data on NVM */

// growing up (like heap). Not starting from zero as first few 16 bytes are for
// testing (see testSPI() function)
#define LAST_REPORTED_LAYER_IDX_OFFSET 16
#define INTERMEDIATE_VALUES_OFFSET 256
#define SAMPLES_OFFSET \
  (INTERMEDIATE_VALUES_OFFSET + NUM_SLOTS * INTERMEDIATE_VALUES_SIZE)
#define PARAMETERS_OFFSET (SAMPLES_OFFSET + SAMPLES_DATA_LEN)

// growing down (like stack)
#define INFERENCE_STATS_OFFSET (NVM_SIZE - INFERENCE_STATS_DATA_LEN)
#define MODEL_OFFSET (INFERENCE_STATS_OFFSET - 2 * MODEL_DATA_LEN)
#define INTERMEDIATE_PARAMETERS_INFO_OFFSET \
  (MODEL_OFFSET - INTERMEDIATE_PARAMETERS_INFO_DATA_LEN)
// Generated node_flags_data already contains two copies for shadowing, so there
// is no need to multiply the size by 2 here
#define NODE_FLAGS_OFFSET \
  (INTERMEDIATE_PARAMETERS_INFO_OFFSET - NODE_FLAGS_DATA_LEN)
#define NODES_OFFSET (NODE_FLAGS_OFFSET - NODES_DATA_LEN)
#define FOOTPRINTS_OFFSET (NODES_OFFSET - FOOTPRINTS_DATA_LEN)
#define COUNTERS_OFFSET (FOOTPRINTS_OFFSET - COUNTERS_DATA_LEN)
#define INFERENCE_RESULTS_OFFSET (COUNTERS_OFFSET - INFERENCE_RESULTS_DATA_LEN)

