// Platform abstraction layer: NVM I/O, memory layout, and MCU-specific helpers.
//
// NVM LAYOUT (FRAM on MSP430/MSP432, simulated file on PC)
// =========================================================
// The NVM is split into two regions that grow toward each other:
//
//   Low addresses (growing up, heap-like):
//     [0,   16)                    reserved / SPI test area
//     [16,  256)                   LAST_REPORTED_LAYER_IDX
//     [256, 256+NUM_SLOTS*IVS)     intermediate activation slots (IVS each)
//     [... + SAMPLES_DATA_LEN)     input test-set samples
//     [... + PARAMETERS_DATA_LEN)  static model weights
//
//   High addresses (growing down, stack-like):
//     NVM_SIZE - INFERENCE_STATS_DATA_LEN   inference stats
//     - 2*MODEL_DATA_LEN                   Model (×2 shadow copies)
//     - INTERMEDIATE_PARAMETERS_INFO_DATA_LEN  ParameterInfo for each node
//     - NODE_FLAGS_DATA_LEN                NodeFlags (already ×2 in data)
//     - NODES_DATA_LEN                     Node descriptors
//     - FOOTPRINTS_DATA_LEN                HAWAII footprints (×2 shadow)
//     - COUNTERS_DATA_LEN                  performance counters
//     - INFERENCE_RESULTS_DATA_LEN         per-sample inference results
//
// The *_DATA_LEN constants and the model binary blobs (nodes_data,
// node_orig_flags_data, model_parameters_info_data, etc.) are generated by
// the Python transform script from an ONNX model and baked into data.h/data.c.

